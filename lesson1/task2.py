"""
2. Пользователь вводит время в секундах. Рассчитайте время и сохраните отдельно
в каждую переменную количество часов, минут и секунд. Переведите время в часы,
минуты, секунды и сохраните в отдельных переменных.
Используйте приведение типов для перевода строк в числовые типы.
Предусмотрите проверку строки на наличие только числовых данных через встроенный
строковый метод .isdigit().
Выведите рассчитанные часы, минуты и секунды по отдельности в консоль.
"""

# Запрашиваем время в секундах от пользователя
input_seconds = input("Введите время в секундах: ")

# Проверяем, что введены только числа
if input_seconds.isdigit():

    # Приводим введенные данные к целому числу
    total_seconds = int(input_seconds)

    # Рассчитываем часы, минуты и секунды
    hours = total_seconds // 3600
    remaining_seconds = total_seconds % 3600
    minutes = remaining_seconds // 60
    seconds = remaining_seconds % 60

    # Выводим результат
    print("Часы:", hours)
    print("Минуты:", minutes)
    print("Секунды:", seconds)
else:
    print("Ошибка! Введите только целое число.")